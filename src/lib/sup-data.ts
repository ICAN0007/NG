// Pre-map all videos to models for performance
const videoMap = new Map<string, Video[]>();
const combinedVideos = [...videos, ...legacySupVideos];

  combinedVideos.forEach(v => {
  if (!v.model) return;
  const models = v.model
    .split(/\s*(?:,|&|\+|\/| and )\s*/i)
    .map((m) => m.trim().toLowerCase())
    .filter(Boolean);
    
  models.forEach(modelName => {
    if (!videoMap.has(modelName)) {
      videoMap.set(modelName, []);
    }
    videoMap.get(modelName)!.push(v);
  });
});

export const supModels: SupModel[] = supMembers.map(member => {
  const customData = modelDataMap[member.name] || modelDataMap[member.id] || {};
  
  const mNameLower = member.name.toLowerCase();
  const mIdLower = member.id.toLowerCase();
  
  const modelVideosRaw = [
    ...(customData.videos || []),
    ...(videoMap.get(mNameLower) || []),
    ...(mIdLower !== mNameLower ? (videoMap.get(mIdLower) || []) : []),
  ];

  // Deduplicate by ID, keeping the first occurrence (which is now customData)
  const dedupedVideos: Video[] = [];
  const seenIds = new Set<string>();
  
  modelVideosRaw.forEach(v => {
    if (!seenIds.has(v.id)) {
      seenIds.add(v.id);
      dedupedVideos.push(v);
    }
  });

  const modelVideos = dedupedVideos.map(v => {
    const cats = new Set([...(v.categories || []), member.platform, "Popular"]);
    return {
      ...v,
      categories: Array.from(cats),
      isSup: true
    };
  });

  const merged = {
    ...member,
    videos: modelVideos,
    ...customData
  } as SupModel;

  // Propagate model photos to video galleries if missing
  // This ensures that model photos show up as a gallery on the watch page
  if (merged.videos && merged.photos) {
    merged.videos = merged.videos.map(v => ({
      ...v,
      gallery: (v.gallery && v.gallery.length > 0) ? v.gallery : merged.photos
    }));
  }
  
  // Ensure we have a portrait and banner even if not provided in customData
  if (!merged.portrait) {
    merged.portrait = merged.image;
  }
  if (!merged.banner) {
    merged.banner = {
      type: 'image',
      url: merged.image
    };
  }

  // Ensure image used in grid is updated to portrait if custom portrait exists but image was default
  if (!customData.image && customData.portrait) {
    merged.image = customData.portrait;
  }

  // Ensure folderThumb uses the best available image
  if (!merged.folderThumb) {
    merged.folderThumb = merged.image;
  }
  
  return merged;
});

